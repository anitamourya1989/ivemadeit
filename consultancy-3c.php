                     <h3>Career Coaching Definition</h3>
                     <span class="topup">[<a href="#">top</a>]</span>
                     <div class="clearfix"></div>
                     <p>The formal definition of career coaching from the International Federation of Coaching (ICF) states that a career coach partners with you in a thought-provoking and creative process that inspires you to maximize your personal and professional potential.</p>
                     <p>Coaches honor you as the expert in your life and work and believe you are creative, resourceful and whole. The core virtue of career coaching is to help people assess their professional situations with a greater degree of curiosity, empathy and compassion to: </p>
                     <ul>
                        <li>Discover, clarify, and align with goal</li>
                        <li>Encourage self-discovery</li>
                        <li>Elicit solutions and strategies</li>
                        <li>Hold you responsible and accountable</li>
                     </ul><br>
                     <p>This process helps clients dramatically shift their attitudes, beliefs, about themselves and their potential to pursue a career linked with their innermost passion.</p>
                     <h3>What does a Career Coach do?</h3>
                     <p>Your coach supports you through your journey in a fair, mature and balanced way to making achievement a matter of habit. Warm encouragement and a customized system will help you execute, believing anything is possible! It's all up to you.</p>
                     <p>Coach helps you focus on :</p>
                     <ul>
                        <li>Your vision, dreams and strategize how you wish to achieve them.</li>
                        <li>Examine and clarify your needs, values and beliefs and develop the goals, sub-goals and micro goals that help you grow - personally and professionally.</li>
                        <li>Define the issues you face, understand the possible causes that can stand in your way of success</li>
                        <li>Deep Dive to understand, analyze and identify solutions to your challenges and helps you accelerate the path to your destination.</li>
                     </ul><br>
                     <p>Remember in the journey, you will cruise or stumble upon obstacles. The Coach will navigate and explore options that may not be top-of-mind for you. The journey will take you through a period of change, stress or growth with clarity and confidence from where you are today, to where you want to be into the future.</p>

