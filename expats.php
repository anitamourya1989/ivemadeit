						<p>Having worked closely with over 50 expats in India and their local and virtual teams confirmed the gulf between cultures and the pressing need for cross-cultural competence.  Our process starts with leaders gaining a deeper understanding of their own cultural blueprint and contrasting this with the complex cultural tapestry of India.  Go beyond respect, tolerance or adaptation, strive to leverage cultural differences: make the most of diversity, proactively look for gems in different cultures, and promote unity in diversity.</p>

