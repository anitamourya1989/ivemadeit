                     Success followed Anita. And Anita followed success. Until one day, it became all too apparent to her that REAL success is a journey that starts within. One day, standing at the peak of her successful advertising career, where she built the image of multi-national brands as Account Director, she realized she hadn’t really reached the peak, because when she stood up there, she saw a valley in the near distance.
                     <br /><br />
                     This was when she exited the world of advertising and entered the tranquil one of meditation and self-realization. She now saw that what she was doing all along, was measuring her achievements to standards set by her peers, the industry, the world… and furthermore, not rewarding her daily triumphs. She calls this her ‘outward’ success. 
                     <br /><br />
                     Following the wisdom of her Eastern roots and teachings of indigenous ideas, she redirected her strengths as a communicator into a powerful understanding which led to the company ‘I’ve Made It” – Training for Success. 
                     <br /><br />
                     Today, Anita is also a keynote speaker, who combines her passion and compassion with a unique ability to convert knowledge  into understanding, to engage people and communities and transform lives.  
