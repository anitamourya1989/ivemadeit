						<a name="2"></a>
						<h3>Are your teams, leaders and Expats in India <i>Culturally Competent</i>?</h3>
						<span class="topup">[<a href="#navtop">top</a>]</span>
						<div class="clearfix"></div>

						<p><em>Do they know that someone from a different culture (like China) may be showing them respect by avoiding eye contact in conversation? Culture might also explain delays in decision making or lack of quick response to emails from colleagues or clients in the Middle East. In the West, it is common for managers to ask for opinions and feedback from teams.  However, in some cultures (like India) that behavior may be interpreted as a weakness in leadership.  &lsquo;Saying it like it is&rsquo; may be direct and works in the USA, it may not work in Japan.</em></p>

						<p>In today&rsquo;s business environment, the new norm is working, meeting, communicating, entertaining, negotiating and generally getting things done with colleagues or clients across geographies and cultures. Hence the ability to communicate effectively across cultures can be complex and stressful.</p>

						<p>Cultural Competence through training &amp; coaching raises awareness by defining both differences and similarities and their importance in the workplace. It promotes clearer communication, breaks down barriers, builds trust, strengthens relationships, opens horizons and yields tangible results in terms of business success. It also provides practical information on how to communicate effectively way beyond a common language (like English or local language).  Body language and other contextual messages are important aspects for leaders communicate across borders.</p>

						<p align="center"><strong><em>&quot;Differences are not intended to separate, to alienate. We are different precisely in order to realize our need of one another.&quot;   Anita Sachdev</em></strong></p>

