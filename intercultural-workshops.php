						<p>Organizations recognize good leaders, however they give little thought to how local leaders become international leaders and what support and training they may need to get them. If getting the most out of expats and executives doing business across borders is so important, why do so many companies get it so wrong?   Is it because most do not believe the impact of culture shock and the cost of culture clash warrant the expense of programs designed to select or train candidates for international assignments despite the evidence?</p>

						<p>The reality of today&rsquo;s global marketplace requires companies to relocate staff to foreign locations to establish and nurture their business presence abroad. Many executives and managers sent to staff foreign operations are chosen for their skills and accomplishments within their native country. The assumption is <em>&lsquo;if they can do it at home, they can do it abroad&rsquo;</em>. Research suggests this is not the case, because over 75% of all international ventures fail due to cross cultural issues. <a href="http://ivemadeit.com/pdf/2012-Brookfield-Global-Relocations-Trends-Survey.pdf" target="_blank">CLICK HERE FOR FULL Research Study PDF</a></p>

