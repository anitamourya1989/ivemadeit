						<p><strong>b. Best Practices on Program Delivery Globally</strong></p>

						<ul class="padleft10">
							<li>Intercultural Program delivery is unique unlike behavioral or soft skills programs.</li>
							<li>The programs are a mix of history, anthropology, cultural models, cultural assessments, opening the mind to the concept &ldquo;Different is different, different is not wrong&rdquo;.</li>
							<li>The focus is on building self-awareness as a leader in a new country followed by building knowledge and skills within the context of the deliverables.</li>
							<li>A country specialist delivers a specialized module virtually for 1.5 hours to fill in any gaps. The local facilitator who has understanding and macro overview of the &ldquo;new country&rdquo; works closely with the participant(s) to unknot the challenges, align with their new country with winning solutions. </li>
